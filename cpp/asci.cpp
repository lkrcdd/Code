#include <iostream>
#include <string>

using namespace std;

int main()
{
    char a = 65;
    int a_ = 65;
    char chr = (char)a_;
    int it = (int)a;
    cout << "char a : " << a << endl;
    cout << "int a_ : " << a_ << endl;
    cout << "int to char : " << chr << endl;
    cout << "char to int : " << it << endl;
}
